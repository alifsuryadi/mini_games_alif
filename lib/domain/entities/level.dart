class Level {
  final int id;
  final String title;
  final String description;
  final String instructions;
  final int startNumber;
  final int endNumber;
  final int step;
  final String icon;

  Level({
    required this.id,
    required this.title,
    required this.description,
    required this.instructions,
    required this.startNumber,
    required this.endNumber,
    required this.step,
    required this.icon,
  });
}
